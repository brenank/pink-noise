from __future__ import annotations

from pink_noise.domain.models import CalibrationProfile


def render_guide(profile: CalibrationProfile) -> str:
    lines = [
        "# Calibration Guide",
        "",
        "## Preflight Checklist",
        "- Confirm you can play local multichannel WAV files without casting, streaming, normalization, or stereo downmixing.",
        "- Start with the master volume low before any reference-volume playback.",
        "- Confirm direct routing: only the speaker named in the file should play. Stop if multiple speakers play unexpectedly.",
        "- Disable upmixing, night mode, dynamic range compression, EQ presets, and volume normalization while checking levels.",
        "",
        "## Safe Volume Setup",
        "1. Set the AVR/player to direct or straight playback.",
        "2. Begin at a low master volume.",
        "3. Play one file and confirm the named speaker only.",
        "4. Raise toward reference only after routing is confirmed.",
        "",
        "## Receiver And Player Setup",
        "- Use bitstream/direct multichannel playback when available.",
        "- Avoid stereo PCM output unless you are intentionally testing a 2.0 layout.",
        "- Set speakers and bass management intentionally before subwoofer checks.",
        "",
        "## SPL Meter Setup",
        "- Place the meter at the main listening position.",
        "- Use C-weighted Slow.",
        "- Point the meter according to the meter maker's instructions and average low-frequency readings longer.",
        "",
        "## 75 dB Speaker Trim Workflow",
        "1. Use the consumer speaker files for normal AVR trim matching.",
        "2. Play one speaker file at a time.",
        "3. Adjust that speaker trim until the meter reads 75 dB SPL.",
        "4. Repeat for every main, surround, and height speaker.",
        "",
        "## File Use Map",
        "- consumer-speaker files: 500 Hz-2 kHz speaker trim material for 75 dB SPL matching.",
        "- subwoofer-lfe-check files: direct LFE-channel checks, not crossover integration.",
        "- subwoofer-bass-managed files: low-frequency main-channel files for summed sub-system level through bass management.",
        "- full-band-analysis files: analyzer/RTA material, not basic SPL trim material.",
        "- pro-reference files: -20 dBFS pro/studio reference material, not consumer AVR defaults.",
        "",
        "## Subwoofer Caveats",
        "- Individual sub checks and summed sub-system level checks answer different questions.",
        "- Bass management must be confirmed before bass-managed files mean anything.",
        "- Phase, delay, crossover, and integration need sweeps or equivalent measurement workflows.",
        "",
        "## Troubleshooting",
        "- Wrong speaker plays: stop and inspect player output mode and channel mapping.",
        "- Multiple speakers play: disable upmixing, room modes, and virtual surround.",
        "- AVR shows stereo input: use a player that can output multichannel WAV directly.",
        "- Meter readings jump around: use Slow averaging and average subwoofer readings longer.",
        "- Playback feels unsafe: turn down immediately and restart the low-volume routing check.",
        "- Casting or streaming changes routing: use local playback from a device connected directly to the AVR.",
        "- Subwoofer mismatch: separate direct LFE checks from bass-managed main-channel checks.",
        "",
        "## Fallback Guidance",
        "Use AVR internal test tones or a measurement workflow if you cannot confirm direct multichannel routing, disable processing, or keep playback safe.",
        "",
        "## Profile Notes",
        profile.measurement_guidance,
    ]
    if profile.warnings:
        lines.extend(["", "## Warnings", *[f"- {warning}" for warning in profile.warnings]])
    return "\n".join(lines) + "\n"
